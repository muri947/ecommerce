                  },
                  style: ElevatedButton.styleFrom(
                    minimumSize: Size.fromHeight(55),
                    backgroundColor: Color(0xFFEf6969),
                    shape: RoundedRectangleBorder(
                      borderRadius: BorderRadius.circular(8),
                    ),
                  ),
                  child: Text(
                    'Validate',
                    style: TextStyle(fontSize: 18, color: Colors.white),
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}

/// You can use any package to retrieve the SMS code. in this example we are using SmartAuth
class SmsRetrieverImpl implements SmsRetriever {
  const SmsRetrieverImpl(this.smartAuth);

  final SmartAuth smartAuth;

  @override
  Future<void> dispose() {
    return smartAuth.removeSmsRetrieverApiListener();
  }

  @override
  Future<String?> getSmsCode() async {
    final signature = await smartAuth.getAppSignature();
    debugPrint('App Signature: $signature');
    // final res = await smartAuth.getSmsCode(useUserConsentApi: true);
    final res = await smartAuth.getSmsWithUserConsentApi(senderPhoneNumber: 'SmartAuth', matcher: 'Your OTP code is: ');
    if (res.data != null) {
      debugPrint('SMS Code: ${res.data!.code}');
      return res.data!.code;
    }
    return null;
  }

  @override
  bool get listenForMultipleSms => false;
}
